from fastapi import APIRouter, Depends, HTTPException, Request
from sqlalchemy.orm import Session
from typing import List
from fastapi.responses import HTMLResponse

import schemas
import crud
from database import get_db

# Создаем отдельные роутеры
api_router = APIRouter()
pages_router = APIRouter()


# ===== HTML PAGES =====
@pages_router.get("/", response_class=HTMLResponse)
async def read_root(request: Request):
    from main import templates
    return templates.TemplateResponse("index.html", {"request": request})


@pages_router.get("/users", response_class=HTMLResponse)
async def read_users_page(request: Request):
    from main import templates
    return templates.TemplateResponse("users.html", {"request": request})


@pages_router.get("/items", response_class=HTMLResponse)
async def read_items_page(request: Request):
    from main import templates
    return templates.TemplateResponse("users.html", {"request": request})


@pages_router.get("/ui", response_class=HTMLResponse)
async def read_ui(request: Request):
    from main import templates
    return templates.TemplateResponse("index.html", {"request": request})


# ===== API ENDPOINTS =====
# User API endpoints
@api_router.post("/users/", response_model=schemas.User)
def create_user(user: schemas.UserCreate, db: Session = Depends(get_db)):
    db_user = crud.get_user_by_email(db, email=user.email)
    if db_user:
        raise HTTPException(status_code=400, detail="Email already registered")
    return crud.create_user(db=db, user=user)


@api_router.get("/users/", response_model=List[schemas.User])
def read_users(skip: int = 0, limit: int = 100, db: Session = Depends(get_db)):
    users = crud.get_users(db, skip=skip, limit=limit)
    return users


@api_router.get("/users/{user_id}", response_model=schemas.User)
def read_user(user_id: int, db: Session = Depends(get_db)):
    db_user = crud.get_user(db, user_id=user_id)
    if db_user is None:
        raise HTTPException(status_code=404, detail="User not found")
    return db_user


@api_router.put("/users/{user_id}", response_model=schemas.User)
def update_user(user_id: int, user: schemas.UserCreate, db: Session = Depends(get_db)):
    db_user = crud.update_user(db, user_id=user_id, user=user)
    if db_user is None:
        raise HTTPException(status_code=404, detail="User not found")
    return db_user


@api_router.delete("/users/{user_id}")
def delete_user(user_id: int, db: Session = Depends(get_db)):
    db_user = crud.delete_user(db, user_id=user_id)
    if db_user is None:
        raise HTTPException(status_code=404, detail="User not found")
    return {"message": "User deleted successfully"}


